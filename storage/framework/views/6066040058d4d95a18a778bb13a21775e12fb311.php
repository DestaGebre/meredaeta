ህወሓት ነዞም ህዝቢ ትግራይ ክሻብ ሕዚ ዘመዝገቦም ዓወታት ክዕቅብን ከስፍሕን ከዕሙቕን ነቶም ዝገጠምዎ ዕንቅፋታት ንሓዋሩ ክፀርግን ነዚ ዝስዕብ ሃገራዊ ፕሮግራም እዚ ኣብ ትግራይ ንምትግባርን ኣብ መላእ ሃገር ንምትግባሩ ከዓ ብፅሒቱ ንምልዓልን ይቃለስ፡፡ መላእ ህዝቢ ትግራይ ኣብ ዙርያ እዚ ዕለማ እዚ ዓሲሉ ንክቃለስ እውን መፀዋዕትኡ የቕርብ፡፡
