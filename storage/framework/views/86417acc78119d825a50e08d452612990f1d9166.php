													  <h3>ሻውዓይ ጉባኤ </h3>
													 <p>ካብ 3 ክሳብ 6 መስከረም 1996 ዓ.ም ኣብ ከተማ መቐለ ተሳሊጡ። “ጭርሖ ብተግባር”፣ “ካብ ድኽነት ነምልጥ” ብዝብሉ ሓሳባት ዝተሰነየ እዚ ጉባኤ ኣብ ውሽጢ ሓሙሽተ ዓመት ውሕስነት ምግቢ ክረጋገፅ ቅዋም ዝተወሰደሉ ጉባኤ እዩ ነይሩ። </p>
