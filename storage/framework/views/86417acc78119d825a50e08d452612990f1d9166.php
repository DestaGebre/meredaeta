                                  <h1>13 ጉባኤታት ህወሓት መዓዝን ኣበይን ከም ዝተኻየዱን ኣብ ሕድ ሕድ ጉባኤ ዝነበረ መሪሕ ጭርሖን</h1> 
                                                     <p>ካብ መስራቲ ጀሚሩ ክሳብ መበል 13 ዝተሳለጡ ሕሉፋት ጉባኤታት ህወሓት መዓዝን ኣበይን እዮም ተሳሊጦም ንዝብል ከምዝስዕብ ተዳልዩ ኣሎ።</p>
													 <h3>መስራቲ ጉባኤ </h3>
													 <p>ካብ 10 ክሳብ 12 ለካቲት 1968 ዓ/ም ኣብ ዞባ መብራቕ ትግራይ፣ ወረዳ ሳዕስዕ ፃዕዳ እምባ ዲማ ማርዋ ኣብ ዝተብሃለ ከባቢ ተሳሊጡ። </p>
													  <h3>ቀዳማይ ጉባኤ </h3>
													 <p>ካብ 17 ክሳብ 21 ለካቲት 1971 ዓ/ም “ሓፋሽ ይወደብ፣ ይንቃሕ፣ ይታዓጠቕ!” ብዝብል መሪሕ ጭርሖ ኣብ ዞባ ምዕራብ ትግራይ ማይ ኣባይ ኣብ ዝተብሃለ ቦታ ተኻይዱ፡፡ </p>
													  <h3>ካልኣይ ጉባኤ </h3>
													 <p>ካብ 2 ክሳብ 8 ግንቦት 1975 ዓ.ም ኣብ ተከዘ ተሳሊጡ።</p>

													<p>እቲ ጉባኤ ኣብ ዝካየድሉ ዝነበረ ኣዳራሽ “ብዘይካ ተሳትፎ ደቂ ኣንስትዮ ዝዕወት ወያነ የለን፣ ማዕርነት ብሄር ብሄረሰባት ብወያናና ይረጋገፅ!” ዝብሉ ጭርሖታት ጎሊሆም ይረኣዩ ነይሮም፡፡

													</p><p>መሪሕ ጭርሕኡ ድማ “ወያነና ኣካል ወያነ ኢትዮጵያ!” ዝብል ኢዩ። </p>
													  <h3>ሳልሳይ ጉባኤ </h3>
													 <p>ካብ 1 ክሳብ 4 መጋቢት 1981 ዓ.ም ኣብ ጥቓ ተከዘ “ኣቤራ” ኣብ ዝተብሃለ ቦታ “ሕብረት ዴሞክራሲያዊያን ሓይልታት ኢትዮጵያ ይጠንክር!” ብዝብል መሪሕ ሓሳብ መሓዙት ውድባት ብዘሳተፈ መልክዑ ተሳሊጡ፡፡ </p>
													  <h3>ራብዓይ ጉባኤ </h3>
													 <p>ካብ 8 ክሳብ 13 ታሕሳስ 1987 ዓ.ም ኣብ ከተማ መቐለ ዝተኻየደ እንትኸውን፥ ድሕሪ ውድቀት ደርጊ ንፈለማ እዋን ዝተሳለጠ ጉባኤ እዩ። </p>
													 <p> ተጋዳላይ “ቃልሲ ተወዲኡ እዩ” ብምባል ናብ ውልቀ ናብራ ዘዘንበለሉ፣ ወነ ቃልሲ ዘህሰሰ ኣካይዳ ዝተረኣየሉን ስለዝነበረ እዚ ንምልዕዓል ክበሃል “ሕዚ እውን ቃልስና ነዊሕን መሪርን እዩ! ዓወትና ናይ ግድን እዩ!” ብዝብል መሪሕ ጭርሖ ዝተሰነየ ነይሩ።</p>
													  <h3>ሓምሻይ ጉባኤ </h3>
													 <p>ካብ 4 ክሳብ 7 ታሕሳስ 1990 ዓ.ም ኣብ ከተማ መቐለ ተኻይዱ። እቲ ጉባኤ  “ንልማዕ” ዝብል ስያመ ዝተዉሃቦ እዩ ነይሩ፡፡ </p>
